            <div class="contenedor-sobre-nosotros">
                <img src="img/img-principal3.jpg" alt="" style="height: 420px; width:45%;  margin: 120px 50px ;" class="imagen-about-us ">
                <div class="contenido-textos">
                    <h3><span>1</span>Reciclaje mecánico</h3>
                    <p>Son todos procesos que incluyan el trabajo manual o ayudado por máquinas. 
                        Este tipo de reciclaje es utilizado mucho en la clasificación de residuos mezclados y en el reciclaje del plástico..</p>
                    <h3><span>2</span>Reciclaje químico</h3>
                    <p>Son aquellos procedimientos que implican el cambio en la estructura química del material. Su finalidad es la descomposición del polímero
                         para la obtención de monómeros. Este tipo de reciclaje es utilizado mediante disoluciones, hidrólisis, hidrogenación.</p>
                         <h3><span>3</span>Reciclaje energético</h3>
                    <p>Se utiliza este tipo de reciclaje en materiales cuyo fin es el aprovechamiento energético y cuyos residuos no pueden ser clasificados y recuperado, por lo que, debido a esta imposibilidad técnica o económica, se aprovechan de este modo.
                         Un ejemplo de estos son la incineración de residuos, pirólisis y gasificación.</p>
                         <h3><span>4</span>Reciclaje por residuo</h3>
                    <p> En este tipo de reiclaje lo que se busca es la degradación de toda la materia orgánica en presencia o en ausencia de oxígeno. Estos tratamientos pueden emplearse con la materia orgánica separada en origen o con la materia orgánica sin separar,
                         utilizando procesos mecánicos complementarios en este último caso.</p>
