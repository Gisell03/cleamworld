        <section class="contenedor sobre-nosotros">
            <h2 class="titulo">Tipo de reciclaje segun el material de residuo</h2>
            <div class="contenedor-sobre-nosotros">
                <img src="img/tipodereciclaje1.webp" alt="" class="imagen-about-us" style="height: 600px; width:40%; margin: 120px 50px ;">
                <div class="contenido-textos">
                    <h3><span>1</span>Reciclaje de papel y carton</h3>
                    <p>Se recogen y se trasladan a planta, una vez allí, se mezclan con agua en una máquina llamada Pulper.
                         Se utilizan aditivos para darle mayor resistencia y blancura y, finalmente, se retiran los impropios como las tintas, grapas etc.
                        Para terminar el proceso, se tira esa pasta resultante en un tapiz  en el cual se van secando y posteriormente produciendo.</p>
                    <h3><span>2</span>Reciclaje de plástico</h3>
                    <p>El plástico es sin duda uno de los residuos que más tardan en degradarse, llegando a tardar la naturaleza hasta cientos de años en degradarlos. El reciclaje de plástico se puede realizar de dos maneras:

                         <b>Mecánica:</b> Que Consiste en recoger el plástico, limpiarlo, molerlo o trocearlo, lavarlo de nuevo y enfardarlo.
                       <b> Químico: </b> Consiste en degradar el plástico para conseguir moléculas simples mediante métodos como:
                        <i><b>Pirólisis: </B></i> Altas temperaturas en ausencia de oxígeno.
                        <i><b>Hidrogenación:</B></i> Altas temperaturas en presencia de hidrógeno.
                        <i><b>Gasificación:</B></i> Convierte el plástico en combustible en presencia de oxígeno o hidrógeno.
                        <i><b>Craqueo:</B></i> Altas temperaturas y catalizador..</p>

                         <h3><span>3</span>Reciclaje de vidrio</h3>
                    <p> las principales ventajas del vidrio es que puede ser reciclado y reutilizado tantas veces como sea necesario dado que, no pierde características ni propiedades.
                        Cuando llega a las fábricas, se separa según el color.
                        Después, se separa de todo el material impropio, como las etiquetas y los tapones.
                        Por último, se tritua y se convierte en polvo se somete a altas temperaturas y se le añade arena, hidróxido de sodio y caliza.</p>
                         <h3><span>4</span>Reciclaje del textil y calzado</h3>
                    <p>La ropa que es recogida y que no puede ser rehutilizada, es deshilada para formar otros productos como mantas, trapos para uso industriales… Si esto no es posible, se incinera para recuperar energía o se lleva al vertedero.</p>
