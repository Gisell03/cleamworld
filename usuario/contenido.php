                    <div class="servicio-ind">
                        <img src="img/Reciclaje-de-Chatarra.jpg" alt="">
                        <h3>Reciclaje de chatarra o metales</h3>
                        <p>Puede ser una mezcla, o no, de metales férricos (aquellos que contienen hierro) y no férricos (aquellos que no contienen hierro). El proceso de reciclaje es mecánico, mediante separación, 
                            cribados y corrientes de Foucault para separar los diferentes residuos, y, para finalizar, se trituran.</p>
                    </div>
                    <div class="servicio-ind">
                        <img src="img/aguasresiduales.jpg" alt="">
                        <h3>Reciclaje de agua</h3>
                        <p>El agua también se recicla. Todo el agua que pasa por nuestros desagües sufre una serie 
                            de tratamientos físico-químicos y biológicos los cuales ayudarán a su limpieza.
                            <i><b>El primer paso</b></i> es tratar el agua con reactivos químicos para eliminar los sólidos que por su pequeño tamaño no ha podido eliminarse en la primera fase..
                             A continuación, se introducen bacterias en el agua para que conviertan la materia orgánica disuelta en ella en flóculos, los cuales pueden ser separados mediante cecantación.
                            <i><b>En el penúltimo paso</b></i>, se aplica un tratamiento químico para eliminar del agua los restos de fosdatos, virus y gérmenes.
                            <i><b>Por último</b></i>, se desinfecta el agua y se prepara para su uso y consumo.
                        </p>
                    </div>
