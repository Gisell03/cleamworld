                        <h3>Reciclaje de pilas y baterías</h3>
                        <p>Son potencialmente peligrosos y muy contaminantes debido a la presencia de metales pesado. Mediante un proceso mecánico se realiza una trituración refrigerada, a continuación, se lava con agua y 
                            pasa por una mesa vibratoria la cual se encarga de separar los metales férreos, no férreos, plástico, papel y polvo de pilas.
Este polvo tiene otro proceso hidrometalúrgico que con ayuda de otros reactivos consigue recuperar los diferentes componentes.</p>
